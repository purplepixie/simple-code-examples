# Iterate (loop through things) example

# Loop through an array (list)
words = ['my','name','is','hal','9000']
for word in words:
    print(word)

# Now a range of numbers
for x in range(5):
    print(x)

# A different way of looping - while a condition is met, continue
x=0
while x<5:
    print(x)
    x=x+1
